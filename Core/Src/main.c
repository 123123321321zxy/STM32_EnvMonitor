void HAL_GPIO_EXTI_Callback(uint16_t GPIO_Pin)
{
	static Key_DataT idata = {0};
	static  uint16_t now = 0;
	static  uint16_t last_time = 0;

	now = __HAL_TIM_GET_COUNTER(&htim2);
	
	if(now - last_time < 40)//20ms，消抖
	{
		last_time = now;
		return;
	}
	
	last_time = now;
	
	//读取电平判断当前是按下还是松开事件
	if(HAL_GPIO_ReadPin(KEY_GPIO_PORT, KEY_GPIO_PIN) == GPIO_PIN_RESET)
	{
		idata.cnt = now;
		idata.event_type = KEY_EVENT_TYPE_PRESS;
		xQueueSendFromISR(g_xKeyQueueHandle, &idata, NULL);
	}
	else
	{
		//松开事件1s超时，不入队列
		if((now - idata.cnt) < 2000)
		{
			idata.cnt = now;
			idata.event_type = KEY_EVENT_TYPE_UNPRESS;
			xQueueSendFromISR(g_xKeyQueueHandle, &idata, NULL);
		}
		else //超时清除之前按下事件入队数据
			memset((uint8_t*)&idata, 0, sizeof(Key_DataT));
	}

}
